# Missão 3
# Crie um programa que que receba dois números inteiros e no final exiba a soma, 
# subtração, multiplicação e divisão entre eles.

print "Digite um número inteiro: "
num1 = gets.chomp.to_i

print "Digite outro número inteiro: "
num2 = gets.chomp.to_i

puts "A soma dos dois números é: #{num1 + num2}."
puts "A subtração dos dois números é: #{num1 - num2}."
puts "A multiplicação dos dois números é: #{num1 * num2}."
puts "A divisão dos dois números é: #{num1 / num2}."